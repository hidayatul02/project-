    private void tabletrans() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("nomor");
        model.addColumn("id barang");
        model.addColumn("nama barang");
        model.addColumn("tanggal transaksi");
        model.addColumn("jumlah beli");
        model.addColumn("jumlah bayar");

        try {
            String udin = "SELECT * FROM transaksi";
            java.sql.Connection con = (Connection) Config.getKoneksi();
            java.sql.PreparedStatement pst = con.prepareStatement(udin);
            java.sql.ResultSet data = pst.executeQuery();
            while (data.next()) {
                model.addRow(new Object[]{
                    data.getString(1),
                    data.getString(2),
                    data.getString(3),
                    data.getString(4),
                    data.getString(5),
                    data.getString(6)
                });
            }

        } catch (Exception e) {
            System.out.println("error" + e.getMessage());
        }

        tabletrans.setModel(model);
    }

