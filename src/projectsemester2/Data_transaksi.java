    private void bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayarActionPerformed
        // TODO add your handling code here:

        String uang = null;
        try {
            String query = "select SUM(jum_bayar) FROM transaksi";
            java.sql.Connection con = (Connection) Config.getKoneksi();
            java.sql.PreparedStatement pst = con.prepareStatement(query);
            java.sql.ResultSet data = pst.executeQuery();
            while (data.next()) {
                uang = data.getString(1);
                int bayar = Integer.parseInt(uang);
                int uangpembeli = Integer.parseInt(JOptionPane.showInputDialog("uang yang harus di bayar " + bayar + "\nmasukan uang anda"));
                int kembalian = uangpembeli - bayar;
                JOptionPane.showConfirmDialog(null, "kembalian anda " + kembalian);

                //cara masukan bilangan kembaalian dah hasil total nya 
                try {

                    String tes = "INSERT INTO transaksi(nama_barang,id_barang,Tanggal_trans,jum_beli,jum_bayar)  VALUES('?','?','?','kembalian  "+kembalian+"','total  "+bayar+"')";
                    java.sql.Connection conn = (Connection) Config.getKoneksi();
                    java.sql.PreparedStatement pstm = conn.prepareStatement(tes);
                    pstm.execute();
                    tabletrans();

                } catch (SQLException e) {
                    System.out.println("error" + e.getMessage());
                }

            }
        } catch (Exception e) {
            System.out.println("error" + e.getMessage());
        }
    }//GEN-LAST:event_bayarActionPerformed

